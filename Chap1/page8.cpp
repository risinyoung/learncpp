#include <iostream>
using namespace std;
int main()
{
    cout << "number of bytes in char : " << sizeof(char) << endl;
    cout << "number of bytes in short : " << sizeof(short) << endl;
    cout << "number of bytes in int : " << sizeof(int) << endl;
    cout << "number of bytes in long : " << sizeof(long) << endl;
    cout << "number of bytes in float : " << sizeof(float) << endl;
    cout << "number of bytes in double : " << sizeof(double) << endl;
    cout << "number of bytes in long double : " << sizeof(long double) << endl;
}